#include <avr/io.h>
#include "ACC_PWM_module.h"
/******************************************************************************
*                      Public functions
******************************************************************************/
void ACC_PWM_start()
{
    SET_BIT(DDRD,PD6);
    TCNT0 =0x00;
    TCCR0A |= ((1<<WGM00)|(1<<WGM01)|(1<<COM0A1)); // fast PWM mode
    TCCR0B |= ((1<<CS00)|((1<<CS01))); //   clock divider by 1024
}

